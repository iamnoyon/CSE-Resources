#include<iostream>

using namespace std;

int main()
{
    cout <<"size of char is " << sizeof(char) << "\n";
    cout <<"size of integer is " << sizeof(int) << "\n";
    cout <<"size of double is " << sizeof(double) << "\n";
    cout <<"size of wchar_t is " << sizeof(wchar_t) << "\n";
    return 0;

}
